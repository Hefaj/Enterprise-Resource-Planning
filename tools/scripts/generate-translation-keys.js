const fs = require('fs');
const path = require('path');

/**
 * Script to automatically generate keys.ts from pl-PL.json translation files.
 * It recursively finds all pl-PL.json files under directories named 'translation'.
 */

function findTranslationFiles(dir, fileList = []) {
  const files = fs.readdirSync(dir);

  for (const file of files) {
    const filePath = path.join(dir, file);
    const stat = fs.statSync(filePath);

    if (stat.isDirectory()) {
      if (file === 'node_modules' || file === '.git' || file === 'dist' || file === '.nx') {
        continue;
      }
      findTranslationFiles(filePath, fileList);
    } else if (file === 'pl-PL.json' && path.basename(dir) === 'translation') {
      fileList.push(filePath);
    }
  }

  return fileList;
}

function getScope(translationDir) {
  const indexPath = path.join(translationDir, 'index.ts');
  if (fs.existsSync(indexPath)) {
    const indexContent = fs.readFileSync(indexPath, 'utf8');
    const scopeMatch = indexContent.match(/scope:\s*['"]([^'"]+)['"]/);
    if (scopeMatch) {
      return scopeMatch[1];
    }
  }
  
  // Fallback: use directory name of parent folder
  const parentDir = path.dirname(translationDir);
  return path.basename(parentDir);
}

function buildKeysObject(jsonObj, scope, currentPath = []) {
  if (typeof jsonObj !== 'object' || jsonObj === null || Array.isArray(jsonObj)) {
    return `${scope}.${currentPath.join('.')}`;
  }
  const result = {};
  for (const [key, val] of Object.entries(jsonObj)) {
    result[key] = buildKeysObject(val, scope, [...currentPath, key]);
  }
  return result;
}

function formatObject(obj, indent = '  ') {
  if (typeof obj === 'string') {
    return `'${obj}'`;
  }
  const lines = [];
  lines.push('{');
  for (const [key, val] of Object.entries(obj)) {
    const formattedKey = /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(key) ? key : `'${key}'`;
    lines.push(`${indent}  ${formattedKey}: ${formatObject(val, indent + '  ')},`);
  }
  lines.push(`${indent}}`);
  return lines.join('\n');
}

function generateKeysFile(plJsonPath) {
  const translationDir = path.dirname(plJsonPath);
  const keysPath = path.join(translationDir, 'keys.ts');
  const scope = getScope(translationDir);
  
  console.log(`Processing translation for scope: "${scope}" at ${plJsonPath}...`);
  
  let plJson;
  try {
    const fileContent = fs.readFileSync(plJsonPath, 'utf8');
    plJson = JSON.parse(fileContent);
  } catch (err) {
    console.error(`Failed to parse JSON file at ${plJsonPath}:`, err.message);
    return;
  }
  
  const keysObj = buildKeysObject(plJson, scope);
  const formattedObj = formatObject(keysObj);
  const constName = `${scope.toUpperCase().replace(/[^A-Z0-9]/g, '_')}_KEYS`;
  
  const fileContent = `/**
 * Typesafe translation keys for the ${scope} scope.
 * Generated automatically from pl-PL.json.
 * Do not edit this file manually.
 */
export const ${constName} = ${formattedObj} as const;
`;

  fs.writeFileSync(keysPath, fileContent, 'utf8');
  console.log(`Successfully generated keys in ${keysPath}`);
}

function main() {
  const frontendDir = path.join(__dirname, '../../frontend');
  if (!fs.existsSync(frontendDir)) {
    console.error(`Frontend directory not found at ${frontendDir}`);
    process.exit(1);
  }

  console.log('Searching for translation files under frontend...');
  const plFiles = findTranslationFiles(frontendDir);
  console.log(`Found ${plFiles.length} translation scope(s).`);

  for (const plFile of plFiles) {
    generateKeysFile(plFile);
  }
  
  console.log('\nAll keys generated successfully!');
}

main();
